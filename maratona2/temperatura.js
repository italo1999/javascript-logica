//classificação de temperatura

let temp = 15;
if (temp < 15){ // frio abaixo de 15
console.log(temp +" temperatura fria")

}
else if(temp >= 15 && temp <= 25 ){     //temperatura entre 15 a 25c
    console.log(temp + "temperatura agradavel");

}
else if(temp >= 26 && temp <= 35 ){
console.log(temp + "temperatura quente");
}
else{
    console.log(temp + "temperatura muito quente");
}

  
    
